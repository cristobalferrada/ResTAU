    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        // TODO add your handling code here:

        try
        {
            Personal per = new Personal();
            per.setRut_personal(txt_rut.getText().trim());
            per.setDigito_v(txt_dvrut.getText().trim());
            per.setNombre_p(txtnom.getText().trim());
            per.setApellido_p(txtape.getText().trim());
            per.setCorreo_p(txtcorreo.getText().trim());
            per.setNumero_p(Integer.parseInt(txtcel.getText().trim()));
             if(jctipo_personal.getSelectedIndex() == 0)
            {
                per.setTip_personal("Sin definir");
            }else {
                per.setTip_personal(jctipo_personal.getSelectedItem().toString());
            }
            if (Manejadora.actualizarPersonal(per))
            {
                JOptionPane.showMessageDialog(null, "Se Modifico Exitosamente");
                txt_rut.setText("");
                txt_dvrut.setText("");
                txtnom.setText("");
                txtape.setText("");
                txtcorreo.setText("");
                txtcel.setText("");
                jctipo_personal.setSelectedIndex(0);
                mostarPersonal();

            } else {
                JOptionPane.showMessageDialog(null, "no se pudo modificar el registro");
            }
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Ingrese Todos los Campos restantes.");
        }

    }//GEN-LAST:event_btn_modificarActionPerformed

