    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
        try
        {
            DefaultTableModel modelo= new DefaultTableModel();
            modelo.addColumn("Rut");
            modelo.addColumn("Digito verificador");
            modelo.addColumn("Nombre personal");
            modelo.addColumn("Apellido personal");
            modelo.addColumn("Celular");
            modelo.addColumn("Correo");
            modelo.addColumn("Tipo Personal");
            this.jTable_mostrar.setModel(modelo);
            try {
                ResultSet rs = Manejadora.buscarPersonal(this.txtrut.getText());
                String [] datos = new String[7];
                while(rs.next()){
                    datos[0]=rs.getString(1);
                    datos[1]=rs.getString(2);
                    datos[2]=rs.getString(3);
                    datos[3]=rs.getString(4);
                    datos[4]=rs.getString(5);
                    datos[5]=rs.getString(6);
                    datos[6]=rs.getString(7);
                    modelo.addRow(datos);
                    this.jTable_mostrar.setModel(modelo);
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(Personal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "no se ha ingresado un rut");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void jTable_mostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_mostrarMouseClicked
        // TODO add your handling code here:
        try
        {
            int x = jTable_mostrar.getSelectedRow();
            String rut = (String)jTable_mostrar.getValueAt(x, 0);
            String dvrut = (String)jTable_mostrar.getValueAt(x, 1);
            String nombre = (String)jTable_mostrar.getValueAt(x, 2);
            String apellido = (String)jTable_mostrar.getValueAt(x, 3);
            String celular = (String)jTable_mostrar.getValueAt(x, 4);
            String correo = (String)jTable_mostrar.getValueAt(x, 5);
            String tipo_pers = (String)jTable_mostrar.getValueAt(x, 6);

            txt_rut.setText(rut);
            txt_dvrut.setText(dvrut);
            txtnom.setText(nombre);
            txtape.setText(apellido);
            txtcorreo.setText(celular);
            txtcel.setText(correo);
            jctipo_personal.setSelectedItem(tipo_pers);

        }catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "no se ha seleccionado ninguna fila");
        }

    }//GEN-LAST:event_jTable_mostrarMouseClicked

